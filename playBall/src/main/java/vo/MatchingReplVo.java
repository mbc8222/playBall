package vo;

public class MatchingReplVo {
private int rno;
private int serial;
private int replSerial;
private String replMid;
private String replDoc;
private String replNal;
private String replVs;

public int getSerial() {
	return serial;
}
public void setSerial(int serial) {
	this.serial = serial;
}
public String getReplMid() {
	return replMid;
}
public void setReplMid(String replMid) {
	this.replMid = replMid;
}
public String getReplDoc() {
	return replDoc;
}
public void setReplDoc(String replDoc) {
	this.replDoc = replDoc;
}
public String getReplNal() {
	return replNal;
}
public void setReplNal(String replNal) {
	this.replNal = replNal;
}
public int getReplSerial() {
	return replSerial;
}
public void setReplSerial(int replSerial) {
	this.replSerial = replSerial;
}
public int getRno() {
	return rno;
}
public void setRno(int rno) {
	this.rno = rno;
}

public String getReplVs() {
	return replVs;
}
public void setReplVs(String replVs) {
	this.replVs = replVs;
}
}
